package com.kmfahey.jchessgame;

public class AlgorithmInternalException extends Exception {

    public AlgorithmInternalException() {
        super();
    }

    public AlgorithmInternalException(final String message) {
        super(message);
    }

    public AlgorithmInternalException(final String message, final Throwable cause) {
        super(message, cause);
    }

    public AlgorithmInternalException(final Throwable cause) {
        super(cause);
    }
}
